def calculate_new_burnout_risk(stressniveau, werkuren, slaaproutine, werkdays):
    """Calculate burnout risk based on the new formula"""
    # Formula: (Stressniveau × 10) + (Werkuren × 5) - (Slaaproutine × 8) ÷ Werkdays
    # Ensure werkdays is not zero to avoid division by zero
    if werkdays == 0:
        return 100 # High risk if no workdays are specified or zero

    risk_score = (stressniveau * 10) + (werkuren * 5) - (slaaproutine * 8) / werkdays

    # Normalize to a percentage, capping at 0 and 100
    # Let's assume a baseline where 0 is very low risk and a high positive score is high risk.
    # This is a simplified mapping, might need adjustment based on desired distribution.
    min_score = 0 # Example baseline
    max_score = 200 # Example upper bound for risk score

    # Simple linear mapping to 0-100%
    percentage = max(0, min(100, (risk_score - min_score) / (max_score - min_score) * 100))
    return round(percentage, 1)


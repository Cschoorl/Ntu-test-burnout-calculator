def get_indicator_dot(metric, value):
    """Returns an HTML span with a colored dot based on metric value"""
    color = 'grey' # Default color

    if metric == 'Sleep Duration':
        if 7.0 <= value <= 9.0:
            color = '#16a34a' # Green
        elif 6.5 <= value < 7.0 or value > 9.0:
            color = '#f97316' # Orange
        else:
            color = '#dc2626' # Red
    elif metric == 'Quality of Sleep':
        if value >= 7:
            color = '#16a34a'
        elif value >= 5:
            color = '#f97316'
        else:
            color = '#dc2626'
    elif metric == 'Stress Level':
        if value <= 4:
            color = '#16a34a'
        elif value <= 6:
            color = '#f97316'
        else:
            color = '#dc2626'
    elif metric == 'Heart Rate':
        if 60 <= value <= 100:
            color = '#16a34a'
        elif value > 100 or value < 60:
            color = '#f97316' # Moderate risk if slightly out of range
        else:
            color = '#dc2626' # High risk if significantly out of range (though not explicitly covered by simple ranges here)
    elif metric == 'Physical Activity Level':
        if value >= 60:
            color = '#16a34a'
        elif value >= 30:
            color = '#f97316'
        else:
            color = '#dc2626'
    elif metric == 'Daily Steps':
        if value >= 7500:
            color = '#16a34a'
        elif value >= 5000:
            color = '#f97316'
        else:
            color = '#dc2626'

    return f"<span style='height: 10px; width: 10px; background-color: {color}; border-radius: 50%; display: inline-block; margin-left: 8px;'></span>"


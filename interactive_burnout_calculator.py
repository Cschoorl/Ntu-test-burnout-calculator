def prepare_features(df):
    """Prepare features for modeling"""
    df_processed = df.copy()

    # Parse Blood Pressure
    df_processed[['Systolic BP', 'Diastolic BP']] = df_processed['Blood Pressure'].str.split('/', expand=True).astype(float)

    # Encode categorical variables

    # Gender: Define all possible categories explicitly for robust fitting
    le_gender = LabelEncoder()
    all_genders = ['Male', 'Female', 'Unknown']
    le_gender.fit(all_genders) # Fit on comprehensive set

    # Process Gender column: convert to string, replace 'None' (string) with 'Unknown', handle empty strings and NaNs
    df_processed['Gender'] = df_processed['Gender'].astype(str).replace('None', 'Unknown').replace('', 'Unknown').fillna('Unknown')
    df_processed['Gender'] = le_gender.transform(df_processed['Gender'])

    # BMI Category: Define all possible categories explicitly for robust fitting
    le_bmi = LabelEncoder()
    all_bmi_categories = ['Underweight', 'Normal', 'Overweight', 'Obese', 'Unknown']
    le_bmi.fit(all_bmi_categories) # Fit on comprehensive set

    # Process BMI Category column: convert to string, replace 'None' (string) with 'Unknown', handle empty strings and NaNs
    df_processed['BMI Category'] = df_processed['BMI Category'].astype(str).replace('None', 'Unknown').replace('', 'Unknown').fillna('Unknown')
    df_processed['BMI Category'] = le_bmi.transform(df_processed['BMI Category'])

    # Sleep Disorder: Define all possible categories explicitly for robust fitting
    le_sleep = LabelEncoder()
    # 'None' is a valid category. Replace empty string/NaN with 'Unknown', but preserve 'None' string.
    all_sleep_disorders = ['None', 'Insomnia', 'Sleep Apnea', 'Restless Leg Syndrome', 'Narcolepsy', 'Unknown']
    le_sleep.fit(all_sleep_disorders) # Fit on comprehensive set

    # Process Sleep Disorder column: convert to string, replace empty string/NaN with 'Unknown'
    df_processed['Sleep Disorder'] = df_processed['Sleep Disorder'].astype(str).replace('', 'Unknown').fillna('Unknown')
    df_processed['Sleep Disorder'] = le_sleep.transform(df_processed['Sleep Disorder'])

    # Occupation: Convert to string, replace 'None' with 'Unknown', empty string with 'Unknown', fill NaNs with 'Unknown'.
    df_processed['Occupation'] = df_processed['Occupation'].astype(str).replace('None', 'Unknown').replace('', 'Unknown').fillna('Unknown')
    df_processed = pd.get_dummies(df_processed, columns=['Occupation'], prefix='Occ', drop_first=True)

    target = df_processed['Burnout Risk']
    features = df_processed.drop(['Person ID', 'Blood Pressure', 'Burnout Risk'], axis=1)

    # Normalize
    scaler = StandardScaler()
    numerical_cols = ['Age', 'Sleep Duration', 'Quality of Sleep', 'Physical Activity Level',
                     'Stress Level', 'Heart Rate', 'Daily Steps', 'Systolic BP', 'Diastolic BP']
    features[numerical_cols] = scaler.fit_transform(features[numerical_cols])

    return features, target, scaler, le_gender, le_bmi, le_sleep

def create_burnout_target(df):
    """Create burnout target variable based on 8 criteria"""
    conditions = [
        df['Stress Level'] >= 7,
        df['Quality of Sleep'] <= 5,
        df['Sleep Duration'] < 6.5,
        df['Physical Activity Level'] < 30,
        df['Heart Rate'] > 90,
        df['Sleep Disorder'] != 'None', # This condition checks for 'None' as a string
        df['BMI Category'].isin(['Overweight', 'Obese']),
        df['Daily Steps'] < 5000
    ]
    risk_count = sum(conditions)
    df['Burnout Risk'] = (risk_count >= 3).astype(int)
    return df


def predict_burnout(person_data):
    """Predict burnout risk for a person"""
    person_df = pd.DataFrame([person_data])
    person_processed = person_df.copy()

    # Parse Blood Pressure
    person_processed[['Systolic BP', 'Diastolic BP']] = person_processed['Blood Pressure'].str.split('/', expand=True).astype(float)

    # Encode categorical
    # Gender: Convert to string, replace 'None' (string) with 'Unknown', empty string with 'Unknown', fill NaNs with 'Unknown'.
    person_processed['Gender'] = person_processed['Gender'].astype(str).replace('None', 'Unknown').replace('', 'Unknown').fillna('Unknown')
    person_processed['Gender'] = le_gender.transform(person_processed['Gender'])

    # BMI Category: Convert to string, replace 'None' (string) with 'Unknown', empty string with 'Unknown', fill NaNs with 'Unknown'.
    person_processed['BMI Category'] = person_processed['BMI Category'].astype(str).replace('None', 'Unknown').replace('', 'Unknown').fillna('Unknown')
    person_processed['BMI Category'] = le_bmi.transform(person_processed['BMI Category'])

    # Sleep Disorder: Convert to string. 'None' is a valid category. Replace empty string/NaN with 'Unknown'.
    person_processed['Sleep Disorder'] = person_processed['Sleep Disorder'].astype(str).replace('', 'Unknown').fillna('Unknown')
    person_processed['Sleep Disorder'] = le_sleep.transform(person_processed['Sleep Disorder'])

    # Occupation: Convert to string, replace 'None' with 'Unknown', empty string with 'Unknown', fill NaNs with 'Unknown'.
    person_processed['Occupation'] = person_processed['Occupation'].astype(str).replace('None', 'Unknown').replace('', 'Unknown').fillna('Unknown')
    person_processed = pd.get_dummies(person_processed, columns=['Occupation'], prefix='Occ', drop_first=True)

    features = person_processed.drop(['Person ID', 'Blood Pressure'], axis=1)

    # Add missing columns (present in training data but not in current person's features)
    for col in X.columns:
        if col not in features.columns:
            features[col] = 0

    # Remove extra columns (present in current person's features but not in training data)
    # This handles new, unseen occupation categories by effectively dropping them
    extra_cols = [col for col in features.columns if col not in X.columns]
    if extra_cols:
        features = features.drop(columns=extra_cols)

    # Select features in correct order
    X_new = features[X.columns]

    # Normalize
    numerical_cols = ['Age', 'Sleep Duration', 'Quality of Sleep', 'Physical Activity Level',
                     'Stress Level', 'Heart Rate', 'Daily Steps', 'Systolic BP', 'Diastolic BP']
    X_new[numerical_cols] = scaler.transform(X_new[numerical_cols])

    # Get predictions
    predictions = {}
    for name, model in models.items():
        pred = model.predict(X_new.values)[0]
        pred_proba = model.predict_proba(X_new.values)[0][1]
        predictions[name] = {'prediction': int(pred), 'probability': float(pred_proba)}

    avg_prob = np.mean([p['probability'] for p in predictions.values()])
    return predictions, float(avg_prob)

"""### BMI Calculator"""

def calculate_bmi(height, weight, height_unit='cm', weight_unit='kg'):
    """Calculate BMI and return category"""
    # Convert to metric
    if height_unit == 'in':
        height = height * 2.54
    height_m = height / 100

    if weight_unit == 'lb':
        weight = weight * 0.453592

    # Calculate BMI
    bmi = weight / (height_m ** 2)

    # Determine category
    if bmi < 18.5:
        category = 'Underweight'
    elif bmi < 25:
        category = 'Normal'
    elif bmi < 30:
        category = 'Overweight'
    else:
        category = 'Obese'

    return round(bmi, 1), category

